void main(){

  // Arithmetic operators

  int a = 10;
  int b = 8;

  int sum = a+b;
  print("$a + $b = $sum");

  int diff = a-b;
  print("$a - $b = $diff");

  int mul = a+b;
  print("$a * $b = $mul");

  int div = a~/b;
  // or double div = a / b;
  print("$a / $b = $div");

  int reminder = a%b;
  print("reminder = $reminder");


  print(a);
  print(a++);
  print(a);
  print(++a);
}